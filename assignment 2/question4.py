# What is tuple unpacking? Provide an example with the tuple coordinates =(10,20,30)

# ANS:-Tuple unpacking is a Python feature that allows you to assign the elements of
# a tuple to multiple variables in a single statement. It's particularly useful when 
# you have functions or operations returning tuples, and you want to assign each element 
# of the tuple to a separate variable.

# EXAMPLE:-

coordinates=(10,20,30)

a,b,c=coordinates

print(a)
print(b)
print(c)